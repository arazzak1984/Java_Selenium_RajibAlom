package Hello_Java;

public class Double_PositiveNegative {

	public static void main(String[] args) {
          
		double x = 9;
		
		if( x>=0.0) {
			System.out.println("positive number");
		}else if(x<=0.0) {
			System.out.println("negative");
		}else {
			System.out.println("number is zero");
		}


	}

}
